<p style="padding:5px; color:#666; letter-spacing:1px; font-size:14px; text-align:justify; line-height:2.2;"><strong>OLAK ROOFING NIGERIA LIMITED</strong> is an incorporated limited liability company registered with the corporate Affairs commission .The incorporation number is  RC 894218 under the Companies and allied matter acts of 1990.The company has been incorporated since twenty-second day of June, 2010 with objects covering different areas of manufacturing value chain development including roofing sheets, international trade, general contract, imports and exports as detailed in memorandum of association of the company.<br>
It is a private liability company with 4 strategic business units (SBUs) that aligned with the revenue streams of the company. The 4 SBUs are the Flat steel galvanizing unit for the production of galvanized roofing sheets, Colour coating unit for the production of galvanized coloured roofing sheets, Corrugation and embossing unit for corrugated and embossed roofing sheets while the 4th unit is the enterprise unit of the company that handles the distribution, sales, marketing and logistics services. <br>
The enterprise unit functions under Ibrahim Olaiya &Sons Limited as a registered business entity. Ibrahim Olaiya & Son Limited is a core investor and indeed the 100% promoter of Olak Roofing Nigeria limited with the provision all equity investment till date. The enterprise unit has 4 sales outlets in Ilorin,kwara state and 1 in Ibadan,Oyo state fully owned by the company as bulk/wholesale distribution centres. The retail end of the distribution chain has over 50 distributors across the countries who have been patronizing the company for over 20 decades. With the compliment of over 17 vehicles out of which 17 are brand new haulage trucks, the transportation of input and products of this project has been seamless.<br>

The company is embarking on the expansion through backward integration of Cold rolled steel Milling line into the already existing galvanizing, roll forming, embossing, corrugation and colour coating line for the production of roofing sheets of different types and forms including customized orders. The aim of the project is to provide an integrated solutions along the entire value chain of quality roofing sheet production for Nigeria market and beyond as a response to government policies on industrialization, job/wealth creation and import substitution.<br>
The company is presently a going concern with a consolidated production facilities located at Ilorin, Kwara State, North central Nigeria on about 1Ha of acquired industrial layout land.

